if __name__ == '__main__':
    # 1. 0 到 4
    for i in range(5):
        print(i, end=" ")
    print()

    # 2. 2 到 6
    for i in range(2, 7):
        print(i, end=" ")
    print()

    # 3. 0 到 10 步长 2
    for i in range(0, 11, 2):
        print(i, end=" ")
    print()

    # 4. 5 到 1 倒序
