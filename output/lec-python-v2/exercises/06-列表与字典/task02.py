if __name__ == '__main__':
    students = [
        {"name": "张三", "score": 85},
        {"name": "李四", "score": 92},
        {"name": "王五", "score": 78}
    ]

    # 方法 1:遍历
    top = students[0]
    for s in students:
        if s["score"] > top["score"]:
            top = s
    print(top["name"])    # 李四
