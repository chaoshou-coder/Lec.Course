if __name__ == '__main__':
    menu = {"红烧肉": 38, "鱼香肉丝": 28,
            "麻婆豆腐": 18, "宫保鸡丁": 26}
    for name, price in menu.items():
        print(f"{name} - {price} 元")

    cheapest_price = min(menu.values())
    for name, price in menu.items():
        if price == cheapest_price:
            print(f"最便宜: {name}")
