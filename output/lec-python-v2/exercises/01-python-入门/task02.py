if __name__ == '__main__':
    # 模拟输入
    total = "3661"
    total_sec = int(total)

    hour = total_sec // 3600          # 小时数
    minute = (total_sec % 3600) // 60  # 剩余分钟
    second = total_sec % 60           # 剩余秒

    print(total + " 秒 = " + str(hour) + " 小时 "
          + str(minute) + " 分钟 " + str(second) + " 秒")
